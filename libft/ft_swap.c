#include "libft.h"

void	ft_pswap(char **a, char **b)
{
	void	*t;

	t = *a;
	*a = *b;
	*b = t;
}

void	ft_nswap(int *a, int *b)
{
	int t;

	t = *a;
	*a = *b;
	*b = t;
}
